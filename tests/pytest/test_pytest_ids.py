from typing import List

import eval_protocol.dataset_logger as dataset_logger
from eval_protocol.dataset_logger.dataset_logger import DatasetLogger
from eval_protocol.models import EvaluationRow, EvaluateResult
from eval_protocol.pytest.default_no_op_rollout_processor import NoOpRolloutProcessor
from tests.pytest.test_markdown_highlighting import markdown_dataset_to_evaluation_row


class InMemoryLogger(DatasetLogger):
    def __init__(self):
        self._rows: dict[str, EvaluationRow] = {}

    def log(self, row: EvaluationRow):
        print(row.execution_metadata.run_id, row.execution_metadata.rollout_id)
        self._rows[row.execution_metadata.rollout_id] = row

    def read(self):
        return list(self._rows.values())


async def test_evaluation_test_decorator(monkeypatch):
    from eval_protocol.pytest.evaluation_test import evaluation_test

    logger = InMemoryLogger()

    @evaluation_test(
        input_dataset=[
            "tests/pytest/data/markdown_dataset.jsonl",
        ],
        completion_params=[{"temperature": 0.0, "model": "dummy/local-model"}],
        dataset_adapter=markdown_dataset_to_evaluation_row,
        rollout_processor=NoOpRolloutProcessor(),
        mode="pointwise",
        combine_datasets=False,
        num_runs=2,
        logger=logger,
    )
    def eval_fn(row: EvaluationRow) -> EvaluationRow:
        row.evaluation_result = EvaluateResult(score=0.0, reason="Dummy evaluation result")
        return row

    dataset_paths = [
        "tests/pytest/data/markdown_dataset.jsonl",
    ]

    # Manually invoke all parameter combinations within a single test
    for ds_path in dataset_paths:
        await eval_fn(dataset_path=[ds_path], completion_params={"temperature": 0.0, "model": "dummy/local-model"})

    # Assertions on IDs generated by the decorator logic
    assert len(logger.read()) == 38


async def test_evaluation_test_decorator_ids_single(monkeypatch):
    in_memory_logger = InMemoryLogger()
    unique_run_ids = set()
    unique_experiment_ids = set()
    unique_rollout_ids = set()
    unique_invocation_ids = set()
    unique_row_ids = set()

    from eval_protocol.pytest.evaluation_test import evaluation_test

    @evaluation_test(
        input_dataset=[
            "tests/pytest/data/markdown_dataset.jsonl",
            "tests/pytest/data/markdown_dataset.jsonl",
        ],
        completion_params=[
            {"temperature": 0.0, "model": "dummy/local-model"},
            {"temperature": 1.0, "model": "dummy/local-model"},
        ],
        dataset_adapter=markdown_dataset_to_evaluation_row,
        rollout_processor=NoOpRolloutProcessor(),
        mode="pointwise",
        combine_datasets=False,
        num_runs=5,
        logger=InMemoryLogger(),
    )
    def eval_fn(row: EvaluationRow) -> EvaluationRow:
        unique_run_ids.add(row.execution_metadata.run_id)
        unique_experiment_ids.add(row.execution_metadata.experiment_id)
        unique_rollout_ids.add(row.execution_metadata.rollout_id)
        unique_invocation_ids.add(row.execution_metadata.invocation_id)
        unique_row_ids.add(row.input_metadata.row_id)
        row.evaluation_result = EvaluateResult(score=0.0, reason="Dummy evaluation result")
        return row

    dataset_paths = [
        "tests/pytest/data/markdown_dataset.jsonl",
        "tests/pytest/data/markdown_dataset.jsonl",
    ]
    completion_params_list = [
        {"temperature": 0.0, "model": "dummy/local-model"},
        {"temperature": 1.0, "model": "dummy/local-model"},
    ]

    # Manually invoke all parameter combinations within a single test
    for ds_path in dataset_paths:
        for params in completion_params_list:
            await eval_fn(dataset_path=[ds_path], completion_params=params)

    # Assertions on IDs generated by the decorator logic
    assert len(unique_invocation_ids) == 1
    assert len(unique_run_ids) == 20  # 4 combinations * 5 runs each
    assert len(unique_experiment_ids) == 2 * 2  # 2 datasets * 2 param sets
    assert len(unique_row_ids) == 19  # from the markdown dataset
    assert len(unique_rollout_ids) == 19 * 5 * 2 * 2  # rows * runs * datasets * params
